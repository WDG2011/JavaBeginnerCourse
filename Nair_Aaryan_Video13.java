package Y10T1;
import java.util.Scanner;

public class Nair_Aaryan_Video13 {

    public static void main(String[] args) {

       int counter = 0; // Setting the value of the counter

        while (counter < 10) { // Repeat the code in the while loop until counter = 10 
            System.out.println(counter); // Printing the value of the counter variable
            counter = counter + 1; // I didn't use counter++ (because you're not supposed to do it in IGCSE exam)
        }
        System.out.println("This program was made by Aaryan Nair on 30th October 2025");
    }
}